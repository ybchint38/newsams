    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(isValidate()){
            Calendar cal=Calendar.getInstance();
            Date ad=new Date(cal.getTimeInMillis());
            Date from=new Date(jDateChooser1.getDate().getTime());
            Date to=new Date(jDateChooser2.getDate().getTime());
            String detail=jTextArea1.getText();
            StudentSql sql=new StudentSql(eNo);
            if(sql.setLeaveDetail(ad, from, to, detail)){
                //System.out.println("Leave Detail saved.");
                setLeaveInTable();
                clearFields();
            }
            sql.close();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

