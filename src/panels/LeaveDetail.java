    private void setLeaveInTable(){
        StudentSql sql=new StudentSql(eNo);
        Vector data=sql.getLeaveDetail();
        sql.close();
        Vector col=new Vector();
        col.add(0, "SNo");
        col.add(1, "Approval Date");
        col.add(2, "From");
        col.add(3, "To");
        col.add(4, "Description");
        DefaultTableModel tm=new DefaultTableModel(data,col){

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
            
        };
        jTable1.setModel(tm);
    }

